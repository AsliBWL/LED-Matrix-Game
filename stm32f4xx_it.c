/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */

  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */
  if (i_left == 109)
  {
	  i_left = i_rem_left;
  }
  if (R[i_left].x == R[i_left + 1].x)
  {
	  for (int i = i_left; i < 110; i++)
	  {
		  if (!(R[i].x == R[i + 1].x))
		  {
			  break;
		  }
		  else
		  {
			  i_left += 1;
		  }
	  }
  }
  if (R[i_right].x == R[i_right + 1].x)
  {
	  for (int i = i_right; i < 110; i++)
	  {
		  if (!(R[i].x == R[i + 1].x))
		  {
			  break;
		  }
		  else
		  {
			  i_right += 1;
		  }
	  }
  }
  //Сила тяжести
  if ((P[0].y > R[i_left].y + 1) && (P[4].y > R[i_right].y + 1))
  {
	  for (int i = 0; i < 15; i++)
	  {
		  P[i].y -= 1;
	  }
  }
  i_left += 1;
  i_right += 1;
  for (int j = 0; j < 110; j++)
  {
	  if (R[j].x == 0)
	  {
		  R[j].x = 81;
	  }
	  else
	  {
		  R[j].x -= 1;
	  }
  }
  for (int i = 0; i < 110; i++)
  {
	  if ((P[4].x == R[i].x) && (P[4].y == R[i].y))
	  {
		  s = 1;
	  }
  }
  /* USER CODE END TIM2_IRQn 1 */
}

